//To update dingoo_gpioreg on a separated thread
void* read_events(void* param) {
	dingoo_dev_gpio = open("/dev/event0", O_RDWR);
	input_event ie;
	while (read(dingoo_dev_gpio, &ie, sizeof(input_event))) {
		unsigned long flag = 0;
		switch (ie.code) {
		case KEY_UP:
			flag = DINGOO_UP;
			break;
		case KEY_DOWN:
			flag = DINGOO_DOWN;
			break;
		case KEY_LEFT:
			flag = DINGOO_LEFT;
			break;
		case KEY_RIGHT:
			flag = DINGOO_RIGHT;
			break;
		case KEY_LEFTCTRL:
			flag = DINGOO_A;
			break;
		case KEY_LEFTALT:
			flag = DINGOO_B;
			break;
		case KEY_SPACE:
			flag = DINGOO_X;
			break;
		case KEY_LEFTSHIFT:
			flag = DINGOO_Y;
			break;
		case KEY_TAB:
			flag = DINGOO_L;
			break;
		case KEY_BACKSPACE:
			flag = DINGOO_R;
			break;
		case KEY_ENTER:
			flag = DINGOO_START;
			break;
		case KEY_ESC:
			flag = DINGOO_SELECT;
			break;
		case KEY_POWER:
			flag = DINGOO_POWER;
			break;
		}
		switch (ie.value) {
		case 0: //key released
			dingoo_gpioreg &= ~flag;
			if (flag == DINGOO_SELECT)
				selectPressed = false;
			break;
		case 1: //key pressed
			dingoo_gpioreg |= flag;
			if (flag == DINGOO_SELECT)
				selectPressed = true;
			break;
		}
	}
}

void dingoo_release_power_slider() {
	dingoo_gpioreg &= ~DINGOO_POWER;
}

unsigned long dingoo_joystick_read(unsigned int use_virtual) {
	unsigned long reg = dingoo_gpioreg;
	/* If using virtual keys, check whether SELECT is being pressed */
	if (use_virtual && selectPressed) {
		reg |= (reg & MASK_SEL_SHIFT) << 7;
		reg &= ~DINGOO_SELECT; /* remove SELECT */
	}
	return reg;
}

void dingoo_joystick_press(void) {
	while (!dingoo_joystick_read(1))
		;
}

